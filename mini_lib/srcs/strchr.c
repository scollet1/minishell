#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;
	size_t	i;

	i = 0;
	while (*s != c)
	{
		if (!*s)
			return (0);
		s++;
	}
	p = (char*)s;
	return (p);
}